package javaoperations;

public class RelationalOperation {
	
public static void main(String[] args) {
		
		int a = 10, b = 5;
		
		//RELATIONAL OPERATIONS
		
		System.out.println(a<b);
		
		System.out.println(a>b);
		
		System.out.println(a!=b);
		
		System.out.println(a==b);	
}
}
