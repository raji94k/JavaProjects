package javaoperations;

public class AssignmentOperation {

	public static void main(String[] args) {
		
		// Assignment Operations
		
		int age,year;
		age = 5;
		year = 10;
		
		System.out.println("age + year = " +(age+=year));
		
		System.out.println("age - year = " +(age-=year));
		
		System.out.println("age * year = " +(age*=year));
		
		System.out.println("age / year = " +(age/=year));
			

	}

}
