package javaoperations;

public class arithmecticoperation {

	public static void main(String[] args) {
		
		int a = 10, b = 5;
		
		//ARITHMETIC OPERATION
		
		//ADDITION OPERATION
		System.out.println("a + b = " +(a+b));
		
		//SUBRACTION OPERATION
		System.out.println("a - b = " +(a-b));
		
		//MULTIPILCATION OPERATION
		System.out.println("a * b = " +(a*b));
		
		//DIVISION OPERATION
		System.out.println("a / b = " +(a/b));
		
		//MODULE OPERATION
		System.out.println("a % b = " +(a%b));
	}
}
